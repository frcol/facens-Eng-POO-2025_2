package Aula_3;

public class Aluno {
    // atributos
    private String nome;
    private String ra;
    private String curso;
    
    // metodos acessores e modificadores
    public String getNome() {
        return nome;
    }
    
    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getRa() {
        return ra;
    }

    public void setRa(String ra) {
        this.ra = ra;
    }

    public String getCurso() {
        return curso;
    }

    public void setCurso(String curso) {
        this.curso = curso;
    }
    
    // metodos Helpers
}
 